export const getServerSideProps = async (context) => {
  const {
    query: { id },
  } = context;

  const jobList = await axios.get(
    `${process.env.NEXT_PUBLIC_API_URL}/v1/user/detail/${id}`
  );

  let convert = jobList?.data;

  return {
    props: {
      profile: convert,
    },
  };
};

